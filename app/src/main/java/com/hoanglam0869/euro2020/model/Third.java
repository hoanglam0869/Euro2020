package com.hoanglam0869.euro2020.model;

public class Third {
    private String firstB, firstC, firstE, firstF;

    public Third(String firstB, String firstC, String firstE, String firstF) {
        this.firstB = firstB;
        this.firstC = firstC;
        this.firstE = firstE;
        this.firstF = firstF;
    }

    public String getFirstB() {
        return firstB;
    }

    public void setFirstB(String firstB) {
        this.firstB = firstB;
    }

    public String getFirstC() {
        return firstC;
    }

    public void setFirstC(String firstC) {
        this.firstC = firstC;
    }

    public String getFirstE() {
        return firstE;
    }

    public void setFirstE(String firstE) {
        this.firstE = firstE;
    }

    public String getFirstF() {
        return firstF;
    }

    public void setFirstF(String firstF) {
        this.firstF = firstF;
    }
}
